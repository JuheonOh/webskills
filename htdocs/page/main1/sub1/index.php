        <h3>FAQ</h3>
        <b>1. 수강신청 후 강좌 수강하기</b>
        <p>메인페이지에서 각 강좌의 개강일을 확인할 수 있으며, 관심있는 강좌는 '자세히 알아보기'를 클릭하면 강좌 소개 영상 및 수강신청을 할 수 있습니다.</p>
        <p>강좌의 개강일과 종료일은 화면 우측에서 확인할 수 있으며, 강좌를 등록한 후 개강일로부터 강좌를 수강할 수 있으니 이점 유념하기 바랍니다.</p>
    </div>
    
    
    <div>
        <b>2. 회원 가입 방법</b>
        <p>회원가입에는 이메일과 아이디가 필수입니다.</p>
        <p>이메일은 인증메일을 받을 수 있도록 실제 이메일 주소를 사용해야 합니다.</p>
        <p>아이디는 영문과 숫자로 이루어진 고유번호이며, 이미 다른 가입자가 사용하고 있다면 다른 아이디를 사용해야 합니다.</p>
        <p>비밀번호는 8자리 이상으로 숫자와 특수기호가 포함된 것을 사용하면 됩니다.</p>
    </div>
    
    <div>
        <b>3. 이용가능한 브라우저</b>
        <p>웹디자인 무크는 인터넷익스플로러10 이상, 최신버전의 크롬, 파이어폭스, 사파리 및 대부분의 스마트패드, 스마트폰을 지원합니다.</p>
        <p>인터넷 익스플로러 9 이전 버전을 이용하고 계시다면 크롬 프라우저 등을 설치하여 이용하기를 권장합니다.</p>
