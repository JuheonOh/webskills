        <h3>웹디자인 무크(Web-Design MOOC)</h3>
        <p><b>MOOC(Massive Open Online Course)란</b> 온라인을 통해서 누구나, 어디서나, 원하는 강의를 무료로 들을 수 있는 온라인 공개강좌 서비스를 말합니다.</p>
        <p>무크(MOOC)는 학습자가 수동적으로 듣기만 하던 기존의 온라인 학습동영상과 달리 교수자와 학습자, 학습자와 학습자간 질의응답, 토론, 퀴즈, 과제 제출 등 양방향 학습이 가능한 새로운 교육 환경을 제공합니다. </p>
        <p>아울러 수강인원의 제한없이 누구나 수강이 가능하여 학습자는 배경지식이 다른 학습자간 지식 공유를 통해 대학의 울타리를 넘어 새로운 학습경험을 하게 될 것입니다.</p>
    </div>
    
    <div>
        <p><b>웹디자인 무크(Web-Design MOOC)는</b> 웹표준 최신 기술을 학습할 수 있는 강좌로 구성되어 있으며, 국내 최고 수준의 강좌를 개발하여 제공하고 있습니다. </p>
        <p>일반 학습자의 경우에는 개인의 역량을 강화하거나 각종 자격, 시험 등을 대비할 수 있습니다. </p>
        <p>기업 재직자의 경우에는 최신 지식정보를 얻거나 직업 업무능력을 계발하는 데 활용할 수 있습니다.</p>
    </div>
    
